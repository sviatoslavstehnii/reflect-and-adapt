#!/usr/bin/env python3
"""
Inspect all adaptations and memory state accumulated during the experiment.

Shows:
  - All proposals generated by Cortex (PENDING / APPROVED / REJECTED)
  - Workspace file evolution across sessions (from snapshots)
  - Final workspace state (IDENTITY.md, USER.md, AGENTS.md, SOUL.md, MEMORY.md)
  - Summary: what changed and when

Usage:
  python analysis/inspect_adaptations.py                        # all personas
  python analysis/inspect_adaptations.py --persona sofia        # one persona
  python analysis/inspect_adaptations.py --persona sofia --diff # show file diffs
"""
from __future__ import annotations

import argparse
import difflib
import os
import sqlite3
import sys
from pathlib import Path

EXPERIMENTS_DIR = Path(__file__).parent.parent
RESULTS_DIR = EXPERIMENTS_DIR / "results"
SESSION_ORDER = ["s01", "s02", "s03", "s04", "s05", "s06", "s07", "s08", "s09", "s10"]
ADAPTIVE_OPENCLAW = os.path.expanduser("~/.openclaw-adaptive")


def _db_path() -> Path | None:
    candidates = [
        Path(ADAPTIVE_OPENCLAW) / "workspace" / ".openclaw" / "extensions" / "reflect-and-adapt" / "reflect.db",
        Path.home() / ".openclaw" / "workspace" / ".openclaw" / "extensions" / "reflect-and-adapt" / "reflect.db",
    ]
    for p in candidates:
        if p.exists():
            return p
    return None


def load_proposals(persona_filter: str | None = None) -> list[dict]:
    db = _db_path()
    if not db:
        return []
    conn = sqlite3.connect(str(db))
    rows = conn.execute(
        "SELECT id, status, proposal_type, target_file, proposed_change, rationale, risk_level, created_at "
        "FROM proposals ORDER BY created_at ASC"
    ).fetchall()
    conn.close()
    cols = ["id", "status", "type", "file", "change", "rationale", "risk", "created_at"]
    proposals = [dict(zip(cols, r)) for r in rows]
    # Filter by persona via session context if needed — proposals don't store persona directly,
    # so we rely on snapshots directory structure for per-persona breakdown
    return proposals


def print_proposals(proposals: list[dict]) -> None:
    if not proposals:
        print("  (none)")
        return

    by_status: dict[str, list] = {}
    for p in proposals:
        by_status.setdefault(p["status"], []).append(p)

    for status in ("APPROVED", "PRESENTED", "PENDING", "REJECTED"):
        group = by_status.get(status, [])
        if not group:
            continue
        print(f"\n  ── {status} ({len(group)}) ──")
        for p in group:
            print(f"  [{p['type']}] {p['file']}  (risk: {p['risk']})")
            print(f"    Rationale: {p['rationale']}")
            if status != "REJECTED":
                change_lines = p["change"].strip().splitlines()
                preview = change_lines[:4]
                print(f"    Change: {chr(10).join('      ' + l for l in preview)}")
                if len(change_lines) > 4:
                    print(f"      ... ({len(change_lines) - 4} more lines)")
            print()


def list_snapshots(persona: str, arm: str = "adaptive") -> list[tuple[str, Path]]:
    """Return (scenario_id, snapshot_dir) pairs in session order."""
    base = RESULTS_DIR / "snapshots" / persona / arm
    if not base.exists():
        return []
    # Dirs are named like s01_video_script — sort by s01/s02/... prefix
    dirs = sorted([d for d in base.iterdir() if d.is_dir()],
                  key=lambda d: d.name)
    return [(d.name, d) for d in dirs]


def print_file_evolution(persona: str, arm: str, show_diff: bool) -> None:
    snapshots = list_snapshots(persona, arm)
    if not snapshots:
        print(f"  No snapshots found at {RESULTS_DIR}/snapshots/{persona}/{arm}/")
        return

    files_to_track = ["IDENTITY.md", "USER.md", "AGENTS.md", "SOUL.md", "MEMORY.md"]
    prev_contents: dict[str, str] = {}

    for scenario_id, snap_dir in snapshots:
        changed: list[str] = []
        diffs: dict[str, str] = {}

        for fname in files_to_track:
            fpath = snap_dir / fname
            if not fpath.exists():
                continue
            current = fpath.read_text()
            prev = prev_contents.get(fname)

            if prev is None:
                changed.append(f"{fname} (initial)")
            elif current != prev:
                changed.append(fname)
                if show_diff:
                    diff = difflib.unified_diff(
                        prev.splitlines(keepends=True),
                        current.splitlines(keepends=True),
                        fromfile=f"{fname} (before {scenario_id})",
                        tofile=f"{fname} (after {scenario_id})",
                        n=2,
                    )
                    diffs[fname] = "".join(diff)

            prev_contents[fname] = current

        if changed:
            print(f"\n  After {scenario_id}: {', '.join(changed)}")
            for fname, diff_text in diffs.items():
                print(f"\n    ── diff: {fname} ──")
                for line in diff_text.splitlines():
                    prefix = "    "
                    if line.startswith("+"):
                        print(f"{prefix}\033[32m{line}\033[0m")
                    elif line.startswith("-"):
                        print(f"{prefix}\033[31m{line}\033[0m")
                    else:
                        print(f"{prefix}{line}")
        else:
            print(f"  After {scenario_id}: (no changes)")

    # Print final state of key files
    print(f"\n  ── Final workspace state ({snapshots[-1][0]}) ──")
    for fname in files_to_track:
        fpath = snapshots[-1][1] / fname
        if fpath.exists():
            print(f"\n  {fname}:")
            for line in fpath.read_text().strip().splitlines():
                print(f"    {line}")


def print_memory_growth(persona: str, arm: str) -> None:
    """Show how MEMORY.md and LanceDB vector memory grew across sessions."""
    snapshots = list_snapshots(persona, arm)
    if not snapshots:
        return

    # MEMORY.md growth
    print("\n  MEMORY.md lines:")
    prev_count = 0
    for scenario_id, snap_dir in snapshots:
        mem = snap_dir / "MEMORY.md"
        if not mem.exists():
            continue
        lines = [l for l in mem.read_text().splitlines() if l.strip() and not l.startswith("#")]
        count = len(lines)
        delta = f"+{count - prev_count}" if count > prev_count else ("=" if count == prev_count else f"{count - prev_count}")
        print(f"    {scenario_id}: {count} lines  ({delta})")
        prev_count = count

    # LanceDB memory entries
    print("\n  Vector memory entries (memory.lance):")
    prev_ids: set[str] = set()
    for scenario_id, snap_dir in snapshots:
        mem_json = snap_dir / "memories.json"
        if not mem_json.exists():
            continue
        import json
        entries = json.loads(mem_json.read_text())
        current_ids = {e["id"] for e in entries}
        new_entries = [e for e in entries if e["id"] not in prev_ids]

        by_type: dict[str, int] = {}
        for e in entries:
            by_type[e["type"]] = by_type.get(e["type"], 0) + 1
        type_summary = ", ".join(f"{t}:{n}" for t, n in sorted(by_type.items()))

        print(f"    {scenario_id}: {len(entries)} total  (+{len(new_entries)} new)  [{type_summary}]")
        for e in new_entries:
            print(f"      [{e['type']}] {e['content'][:100]}")
        prev_ids = current_ids


def run(persona_filter: str | None, show_diff: bool) -> None:
    personas = []
    snap_base = RESULTS_DIR / "snapshots"
    if snap_base.exists():
        personas = sorted(d.name for d in snap_base.iterdir() if d.is_dir())
    if persona_filter:
        personas = [p for p in personas if p == persona_filter]
    if not personas:
        personas = [persona_filter] if persona_filter else ["(unknown)"]

    print("=" * 60)
    print("EXPERIMENT ADAPTATION REPORT")
    print("=" * 60)

    # Global proposals (all sessions, adaptive arm)
    proposals = load_proposals()
    approved   = [p for p in proposals if p["status"] == "APPROVED"]
    presented  = [p for p in proposals if p["status"] == "PRESENTED"]
    pending    = [p for p in proposals if p["status"] == "PENDING"]
    rejected   = [p for p in proposals if p["status"] == "REJECTED"]
    stale      = [p for p in proposals if p["status"] == "STALE"]

    print(f"\n── Proposals summary ──")
    print(f"  Total generated : {len(proposals)}")
    print(f"  Approved        : {len(approved)}")
    print(f"  Presented (applied, awaiting approve cmd) : {len(presented)}")
    print(f"  Still pending   : {len(pending)}")
    print(f"  Rejected        : {len(rejected)}")
    print(f"  Stale (superseded): {len(stale)}")
    print_proposals(proposals)

    for persona in personas:
        print(f"\n{'=' * 60}")
        print(f"PERSONA: {persona}")
        print(f"{'=' * 60}")

        for arm in ("adaptive", "baseline"):
            snaps = list_snapshots(persona, arm)
            if not snaps:
                continue
            print(f"\n── [{arm}] File evolution across {len(snaps)} session(s) ──")
            print_file_evolution(persona, arm, show_diff)

            if arm == "adaptive":
                print(f"\n── [{arm}] Memory growth ──")
                print_memory_growth(persona, arm)


def main() -> None:
    parser = argparse.ArgumentParser(description="Inspect experiment adaptations and memory")
    parser.add_argument("--persona", help="Filter to one persona (e.g. sofia)")
    parser.add_argument("--diff", action="store_true", help="Show unified diffs for changed files")
    args = parser.parse_args()
    run(args.persona, args.diff)


if __name__ == "__main__":
    main()
